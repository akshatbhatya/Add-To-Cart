let basket = JSON.parse(localStorage.getItem("data")) || [];

function calclation() {
    let coterofitems = document.querySelector('#innercount')
    coterofitems.innerHTML = basket.map(x => x.item).reduce((x, y) => x + y, 0);

}
calclation()

